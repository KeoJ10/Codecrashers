<?php

namespace CodeCrashers\Patrick;

class Calculator {
	
	public static function add($x, $y){
		return $x + $y;
	}

	public static function subtract($x, $y){
		return $x - $y;
	}

	public static function multiply($x, $y){
		return $x * $y;
	}

	public static function divide($x, $y){
		return $x / $y;
	}
	
}

?>